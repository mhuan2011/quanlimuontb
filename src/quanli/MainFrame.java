    public void clock() {
        Thread clock = new Thread() {
            public void run() {
                try {
                    while (true) {
                        Calendar cal = new GregorianCalendar();
                        int second = cal.get(Calendar.SECOND);
                        int minute = cal.get(Calendar.MINUTE);
                        int hour = cal.get(Calendar.HOUR_OF_DAY);
                        String thu;
                        int dayOfWeek = cal.get(Calendar.DAY_OF_WEEK);
                        if (dayOfWeek == 1) {
                            thu = "Chủ nhật";
                        } else {
                            thu = "Thứ " + Integer.toString(dayOfWeek);
                        }
                        int dayOfMonth = cal.get(Calendar.DAY_OF_MONTH);
                        int month = cal.get(Calendar.MONTH);
                        int year = cal.get(Calendar.YEAR);

                        jLabel_time.setText(dayOfMonth + "/" + month + "/" + year + " " + hour + ":" + minute + ":" + second);
                        jTextField_NgayGioNhapKho.setText(dayOfMonth + "-" + month + "-" + year + " " + hour + ":" + minute + ":" + second);
                        sleep(1000);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        };
        clock.start();
    }

    //load danh sach tb
    private void docDanhSach() {
        DefaultTableModel dtm = (DefaultTableModel) jTable_TBCoSan.getModel();
        dtm.setNumRows(0);
        String sql = "select * from thietbi";

        try {

            PreparedStatement ps = ketNoi.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Vector v = new Vector();
                String matb, tentb, maloai, ngaynhapkho, mota, tinhtrangsd;
                matb = rs.getString("MATB");
                v.add(matb);
                tentb = rs.getString("TENTB");
                v.add(tentb);
                maloai = rs.getString("MALOAI");
                v.add(maloai);
                ngaynhapkho = rs.getString("NGAYNHAPKHO");
                v.add(ngaynhapkho);
                mota = rs.getString("MOTA");
                v.add(mota);
                tinhtrangsd = rs.getString("TINHTRANGSD");
                demTB(tinhtrangsd);
                v.add(tinhtrangsd);

                dtm.addRow(v);

            }
            rs.close();
            ps.close();
            jTable_TBCoSan.setModel(dtm);
            ghiSoTB();
        } catch (Exception e) {
            System.out.println("Get data error...");
        }
    }

